class PrintCharacters
{
	public static void main(String args[])
	{
	char bigcase;
	System.out.print("Upper case Alphabets are : ");
	for(bigcase='A';bigcase<='Z';bigcase++)
	{
		System.out.print(bigcase+" ");
	}
	char smallcase;
	System.out.print("\nLower case Alphabets are : ");
	for(smallcase='a';smallcase<='z';smallcase++)
	{
		System.out.print(smallcase+" ");
	}
	}
} 